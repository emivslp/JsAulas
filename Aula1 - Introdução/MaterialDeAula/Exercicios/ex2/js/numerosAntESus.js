window.alert("seja bem vindo ao script de Antecessor e Sucessor!");
let numero = parseInt(window.prompt("Digite um numero: "));

const antecessor = numero - 1;
const sucessor = numero + 1;

window.alert(`O número antecessor do número ${numero} é ${antecessor} e o numero sucessor dele é ${sucessor}`);


